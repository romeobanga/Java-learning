package tp.beans;

public class Clients {

    private String nomClients;
    private String prenomClients;
    private String numeroClients;
    private String emailClients;
    private String adresseClients;

    public String getNomClients() {
        return nomClients;
    }

    public void setNomClients( String nomClients ) {
        this.nomClients = nomClients;
    }

    public String getPrenomClients() {
        return prenomClients;
    }

    public void setPrenomClients( String prenomClients ) {
        this.prenomClients = prenomClients;
    }

    public String getNumeroClients() {
        return numeroClients;
    }

    public void setNumeroClients( String numeroClients ) {
        this.numeroClients = numeroClients;
    }

    public String getEmailClients() {
        return emailClients;
    }

    public void setEmailClients( String emailClients ) {
        this.emailClients = emailClients;
    }

    public String getAdresseClients() {
        return adresseClients;
    }

    public void setAdresseClients( String adresseClients ) {
        this.adresseClients = adresseClients;
    }
}
